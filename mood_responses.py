def mood_response(mood):
    if mood == 'happy':
        return "It is good that you are happy!"
    elif mood == 'sad':
        return "Try to cheer yourself up by thinking good thoughts."
    elif mood == 'excited':
        return "Don't be nervous but be excited and eager more."
    else:
        return "That is good. Please Try again later."